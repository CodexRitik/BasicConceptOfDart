//Assignment Operator in dart
void main() {
  var number = 30;
  var number2 = 10;
  number += number2; //number = number + number2
  print(number);
  number -= number2; //number = number - number2
  print(number);
  number ~/= number2; //number = number ~/ number2
  print(number);
  number *= number2; //number = number * number2
  print(number);
  number %= number2; //number = number % number2
  print(number);
}
