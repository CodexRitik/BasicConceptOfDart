//Relational Operator in dart
void main() {
  var number = 30;
  var number2 = 20;
  print("$number > $number2 = ${number > number2}");//greater then
  print("$number < $number2 = ${number < number2}");//less then
  print("$number >= $number2 = ${number >= number2}");//greater or equal
  print("$number <= $number2 = ${number <= number2}");//less or equal
  print("$number == $number2 = ${number == number2}");//equql to
  print("$number != $number2 = ${number != number2}");//not equal

}
